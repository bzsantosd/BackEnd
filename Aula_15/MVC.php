<?php

// o modelo MVC para criação de sistemas

// o que é o padrão MVC?

// este padrao é utilizado para o desenvolvimento limpo e organizado de aplicacoes.

// M -> Model: representa os dados e as regras de negocio.
// ex: Aluno.php(DRUD) e AlunoDAO.php

// V -> View: cuida da parte grafica e da interface com o usuario, ou seja, o que aparece na tela. 
// ex: Index.html ou index.php

// C -> Controller: é o intermediario entre a interface e os dados e regras de negocio. 
// ex: AlunoController.php